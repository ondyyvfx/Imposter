-- ── 2. Несколько шпионов + режим раунда ─────────────────────────────────────
alter table public.rounds add column if not exists impostor_player_ids uuid[] not null default '{}';
alter table public.rounds add column if not exists mode text not null default 'classic';

-- Переносим одиночного шпиона старых раундов в массив.
do $$
begin
  if exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'rounds'
      and column_name = 'impostor_player_id'
  ) then
    update public.rounds
    set impostor_player_ids = array[impostor_player_id]
    where (impostor_player_ids is null or impostor_player_ids = '{}')
      and impostor_player_id is not null;

    -- старую колонку больше не заполняем — снимаем ограничение not null,
    -- чтобы новые вставки без неё проходили.
    alter table public.rounds alter column impostor_player_id drop not null;
  end if;
end $$;
